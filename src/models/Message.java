package models;

/**
 *
 * @author Carlos
 */
public class Message {
    
    private String message;
    private String id;

    public Message(String message, String id) {
        this.message = message;
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public String getId() {
        return id;
    }
    
    
    
}
